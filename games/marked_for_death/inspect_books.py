def main(argv: Optional[List[str]] = None) -> int:
    parser = argparse.ArgumentParser(
        description="Inspect and validate books generated by run.py for Marked for Death (Priority 1 checks)."
    )
    parser.add_argument(
        "book_path",
        nargs="?",
        help="Path to a specific book file (.json, .jsonl, or .zst)",
    )
    parser.add_argument(
        "--latest", action="store_true",
        help="Force the single most recent book file (same as running with no arguments)."
    )
    parser.add_argument(
        "--count", type=int, default=None,
        help="How many of the newest book *files* to inspect (default when no args: 1 = latest)."
    )
    parser.add_argument(
        "--detailed", "--full", action="store_true", dest="detailed",
        help="Show full detailed report for *every* spin (default is summary mode: "
             "overall stats + only problem spins get details; clean spins are one-liners)."
    )
    args = parser.parse_args(argv)

    if args.book_path:
        files = [Path(args.book_path)]
    elif args.latest:
        files = find_book_files(count=1)
    else:
        # Bare run (or just --detailed etc.) → convenient default: latest file + summary
        n = args.count if args.count is not None else 1
        files = find_book_files(count=n)

    if not files:
        print("No book files found. Run `python run.py` first (with small num_sim_args + compression=False).")
        return 1

    total_spins = 0
    total_issues_count = 0
    display_counter = 0
    base_only = 0
    fs_involved = 0
    total_mult_updates = 0
    total_conv_ok = 0
    total_conv_checks = 0

    print(f"inspect_books.py — Marked for Death Priority 1 validation")
    mode = "detailed" if args.detailed else "summary"
    print(f"Mode: {mode}")

    # Collect everything first (for summary-first output)
    all_analyses: List[Dict[str, Any]] = []

    auto_latest = not args.book_path and not args.latest and args.count is None

    for f in files:
        try:
            books = load_books(f)
            prefix = "Auto-selected latest: " if (auto_latest and len(files) == 1) else ""
            print(f"  - {prefix}{f} ({len(books)} spins)")
        except Exception as exc:
            print(f"\n[ERROR] Failed to load {f}: {exc}")
            continue

        for book in books:
            analysis = analyze_book(book)
            all_analyses.append(analysis)

    # Compute aggregates
    for a in all_analyses:
        total_spins += 1
        if a.get("involves_fs"):
            fs_involved += 1
        else:
            base_only += 1

        mults = a.get("mult_progression") or []
        total_mult_updates += len(mults)

        total_conv_ok += a.get("conversion_ok", 0)
        total_conv_checks += a.get("conversion_checks", 0)

        if a.get("issues"):
            total_issues_count += 1

    # Always print overall summary first
    print_overall_summary(
        total_spins=total_spins,
        base_only=base_only,
        fs_involved=fs_involved,
        total_mult_updates=total_mult_updates,
        total_conv_ok=total_conv_ok,
        total_conv_checks=total_conv_checks,
        spins_with_issues=total_issues_count,
        file_count=len(files),
    )

    # Now print per-spin info
    print("\n--- Per-spin results ---")

    problem_analyses = [(i+1, a) for i, a in enumerate(all_analyses) if a.get("issues")]
    clean_analyses = [(i+1, a) for i, a in enumerate(all_analyses) if not a.get("issues")]

    if args.detailed:
        # Full verbose for all
        for idx, a in enumerate(all_analyses, 1):
            print_book_report(a, idx)
    else:
        # Summary mode: full details ONLY for problems
        if problem_analyses:
            print(f"\nProblems ({len(problem_analyses)} spins):")
            for idx, a in problem_analyses:
                print_book_report(a, idx)
        else:
            print("(no spins with issues)")

        # For clean spins: show one-liners only for "interesting" ones (had cascades/mult)
        # + count the rest. This keeps output short for 50+ spins.
        interesting_clean = [(idx, a) for idx, a in clean_analyses
                             if a.get("mult_progression")]
        plain_clean_count = len(clean_analyses) - len(interesting_clean)

        if interesting_clean:
            print(f"\nClean spins with activity ({len(interesting_clean)}):")
            for idx, a in interesting_clean:
                print_compact_spin(a, idx)

        if plain_clean_count > 0:
            print(f"  + {plain_clean_count} additional clean spins (no mult updates / zero payout, omitted for brevity)")

    # Final note
    print(f"\n{'='*70}")
    if total_issues_count == 0:
        print(f"SUMMARY: {total_spins} spins checked across {len(files)} file(s) — all clean.")
        print("Good to proceed!")
    else:
        print(f"SUMMARY: {total_spins} spins checked, {total_issues_count} with issues.")
        print("See detailed PROBLEM sections above. Use --detailed for everything.")
        print("Re-run small validation with DEBUG=True in gamestate.py for extra context.")

    return 0 if total_issues_count == 0 else 2


if __name__ == "__main__":
    sys.exit(main())