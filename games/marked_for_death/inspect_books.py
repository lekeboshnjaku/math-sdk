#!/usr/bin/env python3
"""
inspect_books.py

Priority 1 Validation Helper for Marked for Death (Stake Math SDK)

PURPOSE:
  This script makes it easy to quickly inspect and validate books generated by
  `run.py` (especially when using small num_sim_args + compression=False for
  manual event ordering validation).

  It focuses on the critical Priority 1 rules from IMPLEMENTATION_MAPPING_v1.2.md:
    - Correct event emission order (reveal -> winInfo/updateTumbleWin -> 
      internal convert_marked -> updateGlobalMult -> tumbleBoard -> set*Win -> finalWin)
    - Marked symbol conversion timing: must happen AFTER win events are emitted
      but BEFORE the corresponding tumbleBoard (and before next reveal).
    - Global multiplier behavior:
        * Base game starts at x1
        * Free spins start at x2 (explicit updateGlobalMult emitted on FS entry)
        * +1 after every paid cascade (carries across spins within an FS round)
        * Never resets between FS spins

  Run this immediately after a small validation `python run.py` to confirm
  the mechanics and book events are correct before scaling up sim counts or
  enabling optimization/upload.

USAGE EXAMPLES:
  python games/marked_for_death/inspect_books.py                    # Just run it! Auto-picks latest book file + shows clean summary
  python games/marked_for_death/inspect_books.py --count 5          # Inspect the 5 most recent book files
  python games/marked_for_death/inspect_books.py --detailed
  python games/marked_for_death/inspect_books.py library/books/books_base.json

SUPPORTED BOOK FORMATS:
  - Uncompressed (recommended for validation): library/books/books_*.json  (JSON array)
  - Compressed / JSONL: library/publish_files/books_*.jsonl.zst or .jsonl

The script is intentionally simple and self-contained so it is easy to extend
with additional checks (e.g. more detailed board state diffing for marked symbols,
FS retrigger counting, wincap verification, etc.).

DEFAULT BEHAVIOR (Summary Mode):
  By default the script is summary-first for practicality with 50+ spins:
  - Prints a compact overall summary (totals, base vs freegame, mult updates,
    marked conversions, issue count).
  - Spins without issues appear as short one-liners: "Spin #N [basegame] - OK"
  - Only spins with problems get full details printed.
  Use --detailed (or --full) to force the verbose per-spin report for everything.
"""

