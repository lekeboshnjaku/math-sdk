def find_book_files(count: int = 5) -> List[Path]:
    """Find the most recently modified book files.

    Searches common locations relative to this script and common CWDs.
    Prefers uncompressed .json for validation runs.
    """
    here = Path(__file__).resolve().parent

    # Possible locations (script-relative + common invocation cwds)
    search_dirs = [
        here / "library" / "books",
        here / "library" / "publish_files",
        Path("games/marked_for_death/library/books"),
        Path("games/marked_for_death/library/publish_files"),
        Path("library/books"),
        Path("library/publish_files"),
        here,  # last resort
    ]

    candidates: List[Path] = []
    patterns = ["books_*.json", "books_*.jsonl", "books_*.zst", "*.jsonl.zst", "books_base*"]

    for d in search_dirs:
        if not d.exists():
            continue
        for pat in patterns:
            for p in d.glob(pat):
                if p.is_file():
                    candidates.append(p)

    # Deduplicate by resolved path + sort by mtime (newest first)
    seen = set()
    unique: List[Path] = []
    for p in candidates:
        rp = p.resolve()
        if rp not in seen:
            seen.add(rp)
            unique.append(p)

    unique.sort(key=lambda p: p.stat().st_mtime if p.exists() else 0, reverse=True)
    return unique[: max(count, 1)]


def _load_jsonl_lines(text_stream) -> List[Dict[str, Any]]:
    books = []
    for line in text_stream:
        line = line.strip()
        if line:
            books.append(json.loads(line))
    return books


def load_books(path: Path) -> List[Dict[str, Any]]:
    """Load one or more books from a file.

    Supports:
      - .json (regular JSON array produced with output_regular_json + compression=False)
      - .jsonl (newline-delimited)
      - .jsonl.zst / *.zst (zstandard compressed JSONL)
    """
    p = str(path).lower()

    if p.endswith(".zst") or ".zst" in p:
        if zstd is None:
            raise RuntimeError(
                "zstandard is required to read compressed books. "
                "Install with: pip install zstandard"
            )
        dctx = zstd.ZstdDecompressor()
        with open(path, "rb") as f:
            with dctx.stream_reader(f) as reader:
                text = io.TextIOWrapper(reader, encoding="utf-8")
                return _load_jsonl_lines(text)

    if p.endswith(".jsonl"):
        with open(path, "r", encoding="utf-8") as f:
            return _load_jsonl_lines(f)

    # Regular .json (array or single object)
    data = json.loads(path.read_text(encoding="utf-8"))
    if isinstance(data, list):
        return data
    if isinstance(data, dict):
        return [data]
    return []


