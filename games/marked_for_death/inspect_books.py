# Analysis helpers
# --------------------------------------------------------------------------- #

WIN_EVENTS = {"winInfo", "updateTumbleWin", "setWin", "setTumbleWin"}
FREE_SPIN_EVENTS = {
    "freeSpinTrigger", "freeSpinRetrigger", "updateFreeSpin",
    "freeSpinEnd", "enterBonus"
}
END_EVENTS = {"setTotalWin", "finalWin", "freeSpinEnd"}


def _get_game_types(events: List[Dict]) -> List[str]:
    gts = []
    for e in events:
        if e.get("type") == "reveal":
            gt = e.get("gameType") or e.get("gametype")
            if gt and gt not in gts:
                gts.append(gt)
    return gts or ["unknown"]


def _extract_mult_values(events: List[Dict]) -> tuple:
    """Return (update_values, win_meta_values).

    We separate them because winInfo meta.globalMult reflects the multiplier
    *used for that specific win calculation*, while updateGlobalMult carries
    the authoritative new value after each paid cascade.
    """
    updates: List[int] = []
    win_metas: List[int] = []
    for e in events:
        t = e.get("type")
        if t == "updateGlobalMult":
            v = e.get("globalMult")
            if isinstance(v, (int, float)):
                updates.append(int(v))
        elif t == "winInfo":
            for w in (e.get("wins") or []):
                m = w.get("meta", {}).get("globalMult")
                if isinstance(m, (int, float)):
                    win_metas.append(int(m))
    return updates, win_metas


def _compact_sequence(events: List[Dict], max_len: int = 40) -> str:
    seq = [e.get("type", "?") for e in events]
    s = " → ".join(seq)
    if len(s) > max_len:
        s = s[:max_len] + " …"
    return s


def analyze_book(book: Dict[str, Any]) -> Dict[str, Any]:
    """Analyze a single book for ordering, mult, and marked-conversion timing."""
    events: List[Dict] = book.get("events", []) or []
    book_id = book.get("id", "?")
    criteria = book.get("criteria", book.get("betmode", "unknown"))
    payout = book.get("payoutMultiplier", book.get("payout", 0))

    spin_types = _get_game_types(events)
    spin_type_str = " / ".join(spin_types)

    seq = [e.get("type") for e in events]
    seq_str = _compact_sequence(events)

    # FS involvement must be computed early for use in multiplier checks (tolerate FS-start reset to x2)
    involves_fs = any("free" in (s or "").lower() for s in spin_types) or any(
        t in FREE_SPIN_EVENTS for t in seq
    )

    mult_updates, mult_win_metas = _extract_mult_values(events)
    mult_progress = mult_updates  # authoritative for progression + increment checks

    issues: List[str] = []

    # --- Basic structural checks ---
    if not events:
        issues.append("Book has no events")
        return _make_report(book_id, criteria, payout, spin_type_str, mult_progress, seq, seq_str, issues)

    if seq[0] != "reveal":
        issues.append("First event is not 'reveal'")

    # --- Multiplier progression checks ---
    # Expect strictly increasing by 1 on updateGlobalMult events (the source of truth)
    # Exception: FS round start resets to x2 (even if base had higher mult from cascades).
    # We tolerate a drop to exactly 2 at the start of a freegame round.
    update_indices = [i for i, t in enumerate(seq) if t == "updateGlobalMult"]
    for i in range(1, len(mult_updates)):
        prev, cur = mult_updates[i-1], mult_updates[i]
        if cur == prev + 1:
            continue
        if cur == 2 and involves_fs:
            # FS round start at x2 (after possible base growth) - expected reset, not a violation
            continue
        issues.append(f"Multiplier did not increment by +1 (saw {prev} -> {cur})")

    if involves_fs and mult_updates:
        # Look for a 2 appearing reasonably early (for FS start detection)
        early = mult_updates[:4]
        if 2 not in early and not any(m >= 2 for m in early):
            issues.append("Freegame/FS detected but no globalMult=2 observed near start (FS should begin at x2)")

    # --- Cascade / ordering checks (core Priority 1 validation) ---
    # For this game the expected paid-cascade pattern after a win is:
    #   winInfo / updateTumbleWin  -->  updateGlobalMult  -->  tumbleBoard
    # The "convert_marked_to_wild" call happens between them (silent in events).
    win_indices = [i for i, t in enumerate(seq) if t in WIN_EVENTS]
    tumble_indices = [i for i, t in enumerate(seq) if t == "tumbleBoard"]
    update_g_indices = update_indices

    conversion_checks = 0
    conversion_ok = 0

    for ti in tumble_indices:
        preceding_wins = [wi for wi in win_indices if wi < ti]
        if not preceding_wins:
            # tumble with no win is suspicious for a ways game (tumble only follows wins)
            issues.append(f"tumbleBoard (idx {ti}) has no preceding win* event")
            continue

        last_win = preceding_wins[-1]
        inter_updates = [gi for gi in update_g_indices if last_win < gi < ti]

        conversion_checks += 1
        if inter_updates:
            conversion_ok += 1
        else:
            # Only flag as issue when there was a real win (non-zero payout nearby)
            # Some zero-win paths legitimately skip the mult-update + tumble
            if any(e.get("totalWin", 0) > 0 for e in events[max(0, last_win-1):ti+1] if isinstance(e, dict)):
                issues.append(
                    f"Marked conversion timing suspect: tumbleBoard at {ti} "
                    "not preceded by updateGlobalMult after win event"
                )

    # If we saw wins that led to cascades but never saw the expected update->tumble link
    if conversion_checks > 0 and conversion_ok == 0 and any(
        t in ("tumbleBoard", "updateGlobalMult") for t in seq
    ):
        issues.append("No valid win -> updateGlobalMult -> tumbleBoard pattern detected for cascades")

    # updateGlobalMult should normally be preceded by a win in this implementation
    for gi in update_g_indices:
        preceding = [wi for wi in win_indices if wi < gi]
        if not preceding:
            issues.append(f"updateGlobalMult at idx {gi} without a preceding win event")

    # Final sanity: most books should end with a terminal win event
    if seq and seq[-1] not in END_EVENTS:
        # Not always fatal (some paths), but worth noting
        if payout > 0:
            issues.append(f"Book ends with '{seq[-1]}' instead of a terminal win event")

    return _make_report(
        book_id, criteria, payout, spin_type_str,
        mult_updates, seq, seq_str, issues,
        conversion_checks=conversion_checks,
        conversion_ok=conversion_ok,
        involves_fs=involves_fs,
        win_mults_used=mult_win_metas,
    )


def _make_report(
    book_id: Any,
    criteria: str,
    payout: Any,
    spin_type: str,
    mult_progress: List[int],
    seq: List[str],
    seq_str: str,
    issues: List[str],
    **extra,
) -> Dict[str, Any]:
    return {
        "id": book_id,
        "criteria": criteria,
        "payout": payout,
        "spin_type": spin_type,
        "mult_progression": mult_progress,
        "event_sequence": seq,
        "seq_str": seq_str,
        "issues": issues,
        "conversion_checks": extra.get("conversion_checks", 0),
        "conversion_ok": extra.get("conversion_ok", 0),
        "involves_fs": extra.get("involves_fs", False),
        "win_mults_used": extra.get("win_mults_used", []),
    }


def print_book_report(analysis: Dict[str, Any], book_index: int) -> None:
    """Pretty-print a single book's analysis."""
    a = analysis
    header = f"Book #{book_index}  |  id={a['id']}  |  criteria={a['criteria']}  |  payout={a['payout']}"
    print(f"\n{'='*70}")
    print(header)
    print(f"Spin type: {a['spin_type']}")
    print(f"Multiplier updates (new values from updateGlobalMult): {a['mult_progression'] or 'n/a'}")
    if a.get("win_mults_used"):
        print(f"Win meta globalMults (used for individual win calcs): {a['win_mults_used']}")

    print(f"Event sequence: {a['seq_str']}")

    cc = a.get("conversion_checks", 0)
    co = a.get("conversion_ok", 0)
    if cc > 0:
        status = "OK" if co == cc else "ISSUES"
        print(f"Marked conversion windows (win* -> updateGlobalMult -> tumbleBoard): {co}/{cc}  [{status}]")

    if a["issues"]:
        print("PROBLEMS DETECTED:")
        for msg in a["issues"]:
            print(f"  [!] {msg}")
    else:
        print("[OK] No obvious ordering or multiplier issues detected for this spin.")


def print_compact_spin(analysis: Dict[str, Any], book_index: int) -> None:
    """One-line summary for clean spins (used in default summary mode)."""
    a = analysis
    st = a["spin_type"]
    issues = a.get("issues", [])
    mults = a.get("mult_progression") or []
    mult_str = f"mult:{mults}" if mults else "no mult updates"
    if issues:
        # Should not normally be called for issues, but fallback
        print(f"Spin #{book_index} [{st}] - ISSUES ({len(issues)})")
    else:
        print(f"Spin #{book_index} [{st}] - OK  ({mult_str}, payout={a['payout']})")


def print_overall_summary(
    total_spins: int,
    base_only: int,
    fs_involved: int,
    total_mult_updates: int,
    total_conv_ok: int,
    total_conv_checks: int,
    spins_with_issues: int,
    file_count: int,
) -> None:
    """Clean top-level summary (always shown)."""
    print("\n" + "=" * 70)
    print("OVERALL SUMMARY")
    print(f"Files analyzed : {file_count}")
    print(f"Total spins    : {total_spins}")
    print(f"  Basegame only     : {base_only}")
    print(f"  Involving freegame: {fs_involved}")
    print(f"Multiplier updates seen : {total_mult_updates}")
    conv_pct = f" ({total_conv_ok}/{total_conv_checks})" if total_conv_checks else ""
    print(f"Marked conv. windows OK : {total_conv_ok}{conv_pct}")
    print(f"Spins with issues       : {spins_with_issues}")
    print("=" * 70)


